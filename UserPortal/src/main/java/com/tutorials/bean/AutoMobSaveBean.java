package com.tutorials.bean;

public class AutoMobSaveBean {
	
	private String month="";
	private String amountToPaid="";
	private String dateOfPaid="";
	private String bikeName="";

	
	
	public String getBikeName() {
		return bikeName;
	}
	public void setBikeName(String bikeName) {
		this.bikeName = bikeName;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getAmountToPaid() {
		return amountToPaid;
	}
	public void setAmountToPaid(String amountToPaid) {
		this.amountToPaid = amountToPaid;
	}
	public String getDateOfPaid() {
		return dateOfPaid;
	}
	public void setDateOfPaid(String dateOfPaid) {
		this.dateOfPaid = dateOfPaid;
	}
	
	
	

}
