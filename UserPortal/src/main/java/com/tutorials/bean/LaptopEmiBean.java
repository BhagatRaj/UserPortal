package com.tutorials.bean;

public class LaptopEmiBean {

	private String lapName;
	private String lapModel;
	private String lapPurchaseDate;
	private int lapPurchaseEmi;
	
	
	public String getLapPurchaseDate() {
		return lapPurchaseDate;
	}
	public void setLapPurchaseDate(String lapPurchaseDate) {
		this.lapPurchaseDate = lapPurchaseDate;
	}
	public int getLapPurchaseEmi() {
		return lapPurchaseEmi;
	}
	public void setLapPurchaseEmi(int lapPurchaseEmi) {
		this.lapPurchaseEmi = lapPurchaseEmi;
	}
	public String getLapName() {
		return lapName;
	}
	public void setLapName(String lapName) {
		this.lapName = lapName;
	}
	public String getLapModel() {
		return lapModel;
	}
	public void setLapModel(String lapModel) {
		this.lapModel = lapModel;
	}
		
}
